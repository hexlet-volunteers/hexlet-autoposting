import { fileURLToPath, URL } from 'node:url'
import { defineConfig } from 'vite'
import react from '@vitejs/plugin-react'

// https://vite.dev/config/
export default defineConfig({
  plugins: [react()],
  resolve: {
    alias: {
      '@': fileURLToPath(new URL('./src', import.meta.url)),
    },
  },
  server: {
    port: 5173,
    // API lives under the `/api` prefix so SPA client routes (/posts, /platforms, …)
    // never collide with API paths. In dev we proxy `/api/*` to the Go backend and
    // strip the prefix (backend serves `/posts`, `/auth/...`). See shared/config/env.ts.
    proxy: {
      '/api': {
        target: 'http://localhost:8080',
        changeOrigin: true,
        rewrite: (path) => path.replace(/^\/api/, ''),
      },
    },
  },
})
